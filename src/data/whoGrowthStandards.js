function lmsToValue(L, M, S, z) {
  if (Math.abs(L) < 0.001) return M * Math.exp(S * z)
  const base = 1 + L * S * z
  if (base <= 0) return null
  return M * Math.pow(base, 1 / L)
}

export function getWHOZScore(gender, month, z) {
  if (month < 0 || month > 60) return null
  const m = Math.round(month)
  const data = gender === 'L' ? lmsL : lmsP
  return lmsToValue(data.L[m], data.M[m], data.S[m], z)
}

export function getWHOZCurves(gender, months) {
  const zScores = [-3, -2, -1, 0, 1, 2, 3]
  return zScores.map(z => ({
    z,
    label: z === 0 ? 'Median' : (z > 0 ? `+${z}SD` : `${z}SD`),
    data: months.map(m => getWHOZScore(gender, m, z))
  }))
}
